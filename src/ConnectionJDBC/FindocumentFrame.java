    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String provice =String.valueOf(TypeComboBox.getSelectedItem());
        String IdTL=IDField.getText();
        if(provice=="Book"){
            ShowTable.setModel(defaultTableModel);
            defaultTableModel.addColumn("IdTaiLieu");
            defaultTableModel.addColumn("NameNXB");
            defaultTableModel.addColumn("Release Number");
            defaultTableModel.addColumn("Type Document");
            defaultTableModel.addColumn("Name Author");
            defaultTableModel.addColumn("Page");
            
         
            setTableData1(userService.FindBOOK(IdTL));
            
        }
        
        if(provice=="Jounary"){
            ShowTable.setModel(defaultTableModel);
            defaultTableModel.addColumn("IdTaiLieu");
            defaultTableModel.addColumn("NameNXB");
            defaultTableModel.addColumn("Release Number");
            defaultTableModel.addColumn("Ttpse Document");
            defaultTableModel.addColumn("Name Author");
            defaultTableModel.addColumn("Page");
        
        
        
            setTableData2(userService.FindJounary(IdTL));
        }
        if(provice=="Newspaper" ){
            ShowTable.setModel(defaultTableModel);
            defaultTableModel.addColumn("IdTaiLieu");
            defaultTableModel.addColumn("NameNXB");
            defaultTableModel.addColumn("Release Number");
            defaultTableModel.addColumn("Ttpse Document");
            defaultTableModel.addColumn("Name Author");
            defaultTableModel.addColumn("Page");
        
        
        
            setTableData3(userService.FindNewspaper(IdTL));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

